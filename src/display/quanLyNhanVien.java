    private void aTT() {
        try {
            ls = NhanVienDB.getAll();

            model = (DefaultTableModel) tbl1.getModel();
            model.setRowCount(0);

            for (NhanVien nv : ls) {
                model.addRow(new Object[]{nv.getMaNV(), nv.getmK(), nv.getHoTen(), nv.getRole()});
            }
        } catch (DBException | IOException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }
    }

    private void clear() {
        txtMnv.setText("");
        pswpsw.setText("");
        txtName.setText("");
        rd0.setSelected(true);
        tbl1.clearSelection();
    }

    private void showInfo() {
        NhanVien nv = ls.get(index);
        txtMnv.setText(nv.getMaNV());
        pswpsw.setText(nv.getmK());
        txtName.setText(nv.getHoTen());
        if (nv.getRole().equals("1")) {
            rd0.setSelected(true);
        } else {
            rd1.setSelected(true);
        }
    }

    private boolean checkNull() {
        if (txtMnv.getText().equals("") || pswpsw.getText().equals("") || txtName.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đủ thông tin!");
            return false;
        } else {
            return true;
        }
    }

