    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
      try{
            if (checkNull()){
                if (validate.checkDuplicate("MANHANVIEN", "NHANVIEN", txtMnv.getText())){
                    if (validate.checkPass(pswpsw.getText())){
                        if (validate.checkName(txtName.getText())){
                            NhanVien nv = new NhanVien();
                            nv.setMaNV(txtMnv.getText());
                            nv.setmK(pswpsw.getText());
                            nv.setHoTen(txtName.getText());
                            if (rd0.isSelected()){
                                nv.setRole("1");
                            }
                            else{
                                nv.setRole("0");
                            }

                            NhanVienDB.addNV(nv);
                            JOptionPane.showMessageDialog(this, "SUCCESS!");
                            aTT();
                            onButton();
                            for (int i = 0; i < ls.size(); i++){
                                    if (txtMnv.getText().equals((String) tbl1.getValueAt(i, 0))){
                                        index = i;
                                        tbl1.setRowSelectionInterval(index, index);
                                    break;
                                }
                            }
                        }   
                    }
                }
            }
        }catch(Exception e){
            
        } 

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (checkNull()) {
            if (validate.checkPass(pswpsw.getText())) {
                if (validate.checkName(txtName.getText())) {
                    NhanVien nv = ls.get(index);
                    nv.setMaNV(txtMnv.getText());
                    nv.setmK(pswpsw.getText());
                    nv.setHoTen(txtName.getText());
                    if (rd0.isSelected()) {
                        nv.setRole("1");
                    } else {
                        nv.setRole("0");
                    }

                    try {
                        NhanVienDB.updateNV(nv);
                        aTT();
                        JOptionPane.showMessageDialog(this, "Thành công!");
                    } catch (Exception e) {
                    }
                }
            }
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed

        if (ulti1.confirmAction("Bạn chắc chứ?")) {

            index = tbl1.getSelectedRow();
            NhanVien nv = ls.get(index);

            try {
                NhanVienDB.delNV(nv);
                JOptionPane.showMessageDialog(this, "Thành công!");
                aTT();
                clear();
                offButton();
            } catch (Exception e) {
            }

        }         // TODO add your handling code here:
    }//GEN-LAST:event_btnDelActionPerformed

